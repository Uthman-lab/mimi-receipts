import 'package:flutter/material.dart';

class AppSpacing {
  // Padding
  static const double paddingXS = 4.0;
  static const double paddingS = 8.0;
  static const double paddingM = 16.0;
  static const double paddingL = 24.0;
  static const double paddingXL = 32.0;
  static const double paddingXXL = 48.0;

  // Margins
  static const double marginXS = 4.0;
  static const double marginS = 8.0;
  static const double marginM = 16.0;
  static const double marginL = 24.0;
  static const double marginXL = 32.0;
  static const double marginXXL = 48.0;

  // Edge insets
  static const EdgeInsets edgeInsetsXS = EdgeInsets.all(paddingXS);
  static const EdgeInsets edgeInsetsS = EdgeInsets.all(paddingS);
  static const EdgeInsets edgeInsetsM = EdgeInsets.all(paddingM);
  static const EdgeInsets edgeInsetsL = EdgeInsets.all(paddingL);
  static const EdgeInsets edgeInsetsXL = EdgeInsets.all(paddingXL);

  // Horizontal padding
  static const EdgeInsets horizontalS = EdgeInsets.symmetric(horizontal: paddingS);
  static const EdgeInsets horizontalM = EdgeInsets.symmetric(horizontal: paddingM);
  static const EdgeInsets horizontalL = EdgeInsets.symmetric(horizontal: paddingL);

  // Vertical padding
  static const EdgeInsets verticalS = EdgeInsets.symmetric(vertical: paddingS);
  static const EdgeInsets verticalM = EdgeInsets.symmetric(vertical: paddingM);
  static const EdgeInsets verticalL = EdgeInsets.symmetric(vertical: paddingL);
}

